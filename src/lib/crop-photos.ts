/** Best photo for a plant name: exact match, then first-word match. */
export function plantPhoto(name: string): string | undefined {
  const key = name.toLowerCase().trim();
  const exact = cropPhotos[key];
  if (exact && (exact.plant || exact.harvest)) return exact.plant ?? exact.harvest;
  const first = key.split(/[\s,(]+/)[0];
  if (!first) return undefined;
  for (const [candidate, set] of Object.entries(cropPhotos)) {
    if (candidate.startsWith(first) && (set.plant || set.harvest)) return set.plant ?? set.harvest;
  }
  return undefined;
}
